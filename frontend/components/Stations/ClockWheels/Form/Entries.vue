                                >
                                    <icon-ic-add />
                                </button>
                                <button
                                    type="button"
                                    class="btn btn-outline-secondary cw-action-btn"
                                    :title="$gettext('Duplicate')"
                                    @click="props.duplicateEntry(index)"
