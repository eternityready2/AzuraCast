        <template v-else-if="!isTemplate && templateOptions.length > 0">
            <form-group-select
                id="clock_wheel_template"
                class="mb-3"
                :field="r$.template_id"
                :label="$gettext('Clock template')"
                :options="templateSelectOptions"
                :description="$gettext('Optional. Link this wheel to a reusable template layout.')"
            />

            <form-group-checkbox
                id="inherits_template_slots"
                class="mb-3"
                :field="r$.inherits_template_slots"
                :label="$gettext('Inherit template slots')"
                :description="$gettext('When enabled, slot layout is copied from the template and updates when the template changes. Disable to edit slots on this wheel only.')"
                :disabled="!form.template_id"
            />
        </template>

        <div
            v-if="form.separation_enabled"
            class="row mb-3"
        >
            <div class="col-md-4">
                <form-group-field
                    id="separation_artist_minutes"
                    :field="r$.separation_artist_minutes"
                    :label="$gettext('Artist separation (min)')"
                    type="number"
                />
            </div>
            <div class="col-md-4">
                <form-group-field
                    id="separation_title_minutes"
                    :field="r$.separation_title_minutes"
                    :label="$gettext('Title separation (min)')"
                    type="number"
                />
            </div>
            <div class="col-md-4">
                <form-group-field
                    id="burn_rate_max_plays_24h"
                    :field="r$.burn_rate_max_plays_24h"
                    :label="$gettext('Max plays / 24h')"
                    type="number"
                    :description="$gettext('Leave empty to disable burn-rate deprioritization.')"
                />
            </div>
        </div>

        <div
            v-if="!isTemplate"
            class="alert alert-info py-2 mb-4"
        >
            {{ $gettext('Air times are managed on the station Schedule page (calendar), not here. Create the wheel first, then use Schedule → Create Event to assign it.') }}
        </div>

        <div
            v-if="!isTemplate && slotsReadOnly"
            class="alert alert-warning py-2 mb-3"
        >
            {{ $gettext('Slots are managed by the linked template. Turn off “Inherit template slots” to edit this wheel’s layout locally.') }}
        </div>

        <div
            class="mb-1"
            :class="{'opacity-50 pe-none': slotsReadOnly}"
        >
            <div class="d-flex align-items-center justify-content-between mb-2">
                <span class="fw-semibold">
                    {{ $gettext('Clockwheel entries') }} ({{ entries.length }})
                </span>
            </div>

            <div
                v-if="entries.length > 0"
                class="clock-wheel-timeline mb-3"
                role="img"
                :aria-label="$gettext('Hour timeline showing anchor positions')"
            >
                <div class="clock-wheel-timeline__track">
                    <span class="clock-wheel-timeline__label clock-wheel-timeline__label--start">0:00</span>
                    <span class="clock-wheel-timeline__label clock-wheel-timeline__label--end">59:59</span>
